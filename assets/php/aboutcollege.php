<h1 style="font-weight:bold">Welcome to P.G.D.A.V.</h1>
                                <p>P.G.D.A.V. College, since its inception in 1957 has been a premier
                                institution for higher learning. The college promotes academic excellence
                                coupled with vibrant co-curricular activities to enable the
                                holistic development of its students. It is one of the oldest constituent
                                of University of Delhi and is located in the periphery of
                                Lajpat Nagar. It offers eleven undergraduate courses and four
                                post graduate courses. <br><br>At P.G.D.A.V. we believe in holistic education that goes beyond
                            achieving academic excellence and vocational training. Our philosophy
                            is to realize the overall intellectual, creative, cultural and
                            socio-political growth and development of both the students and
                            the college community in its entirety.<br>
                            The College has a highly qualified and experienced faculty, well
                            equipped computer labs, a library with over 70,000 books along
                            with various journals and magazines and WiFi facilities that are
                            available to the students everywhere within the campus.
                            P.G.D.A.V. has also nurtured enormous sports talent in the field of
                            cricket. College Alumni include international cricket players like
                            Atul Wassan, Manoj Prabhakar and Raman Lamba who have
                            brought laurels to the country. Moving into a new academic year
                            P.G.D.A.V. College is committed to nurturing the talent of its students
                            and equipping them with unbound skill and excellence. </p>